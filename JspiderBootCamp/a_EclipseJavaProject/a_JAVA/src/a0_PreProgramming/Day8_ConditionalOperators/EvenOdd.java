package a0_PreProgramming.Day8_ConditionalOperators;

class EvenOdd {
    public static void main(String args[])
    {
        int a = 10;

        if(a%2==0)
        {
            System.out.println("The given Number "+a+" is Even"); 
        }
        if(a%2!=0)
        {
            System.out.println("The Given Number" +a+"is Odd");
        }
    }
}
