package a0_PreProgramming.Day9_IfElseSwitch;

class IfElseLadder {
    public static void main(String args[])
    {
        String name = "Patana";

        if(name=="Mumbai")
        {
            System.out.println("You are in Financial city");
        }
        else if(name=="Bangalore")
        {
            System.out.println("You are in It city");
        }
        else if(name=="Delhi")
        {
            System.out.println("You are in Capital city");
        }
        else
        {
            System.out.println("Somewhere in Earth");
        }
    }

}