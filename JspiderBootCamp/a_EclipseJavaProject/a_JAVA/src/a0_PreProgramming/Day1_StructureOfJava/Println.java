package a0_PreProgramming.Day1_StructureOfJava;

class Println //class name
{
    public static void main(String args[])
    {
        System.out.println("Hii Baby"); //After printing this line cursor will go on the next line
        System.out.println("Come Here");   //This statement will move to next line
        System.out.println();   //you can keep empty the println Statement it only move cursor to next line
        System.out.println("");  //This line also move cursor to the next line
        System.out.println("This is how println statement is work ");
    }
}
